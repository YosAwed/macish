void enc_jis8(uchar *argv, int len)
{
	register unsigned short i, j, a;
	register unsigned char *dat;

	argv[len]='\0';
	dat = argv-1;
	for (i = 0; dat < argv+len; ++i) {
		a = 0;
		if ((j = i%8) != 0) a |= (*dat) >> (8 - j);
		if (j != 7) a |= (*++dat) << j;
		a &= 0x7f;
		obuf[i] = ent_j8[a];
	}
	obuf[i]='\0';
}

int dec_jis8(uchar *argv, int len)
{
	register int i, j, c;
	unsigned char *dat;

	if(len > 79) len=79;

	argv[len] = '\0';
	dat = obuf-1;
	for(i = 0; i < len; ++i) {
		if((c = dct_j8[argv[i]]) == 0xff) return 0;
		if((j = i%8) != 0) *dat |= c << (8 - j);
		if(j != 7) *++dat = c >> j;
	}
	return (int)(dat-obuf);
}
